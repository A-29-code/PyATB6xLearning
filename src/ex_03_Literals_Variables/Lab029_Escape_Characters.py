# \' -> Single Quote
print( 'It\'s alright.')
# \\ -> backslash
print("This will insert one \\ (backslash).")
# \n -> New Line
print("Hello\nWorld")
# \r -> Carriage Return
print("Check\r carriage return")
# \t -> Tab
print("HELLO\tWORLD")
# \b -> Backspace
print("Hello \bWorld!")
# \f -> Form Feed
# \ooo -> Octal value
print("\110\145\154\154\157")
# \xhh -> Hex value
print("\x48\x65\x6c\x6c\x6f")